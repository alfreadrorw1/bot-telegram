def get_font_markup(text, page=0):
    buttons = []
    font_styles = list(FONT_MAP.keys())
    items_per_page = 8
    total_pages = (len(font_styles) + items_per_page - 1) // items_per_page
    
    # Get styles for current page
    start_idx = page * items_per_page
    end_idx = start_idx + items_per_page
    current_styles = font_styles[start_idx:end_idx]
    
    # Create 2 buttons per row
    for i in range(0, len(current_styles), 2):
        row = []
        if i < len(current_styles):
            row.append(Button.inline(current_styles[i], data=f"font_{current_styles[i]}_{text}"))
        if i+1 < len(current_styles):
            row.append(Button.inline(current_styles[i+1], data=f"font_{current_styles[i+1]}_{text}"))
        buttons.append(row)
    
    # Add navigation buttons if needed
    nav_buttons = []
    if page > 0:
        nav_buttons.append(Button.inline("â¬…ï¸ Prev", data=f"fontpage_{page-1}_{text}"))
    if page < total_pages - 1:
        nav_buttons.append(Button.inline("Next â¡ï¸", data=f"fontpage_{page+1}_{text}"))
    
    if nav_buttons:
        buttons.append(nav_buttons)
    
    # Add close button
    buttons.append([Button.inline("âœ– Close", data="close")])
    
    return buttons

async def setup(bot, user):
    # Inline handler (BOT)
    @bot.on(events.InlineQuery)
    async def inline_handler(event):
        if event.text and event.text.strip().startswith("font "):
            text = event.text[5:].strip()
            if not text:
                return
                
            try:
                result = event.builder.article(
                    title="ğŸ¨ Font Styles",
                    description=f"Convert: {text}",
                    text=create_font_caption(text, event.sender.first_name),
                    buttons=get_font_markup(text)
                )
                await event.answer([result])
            except Exception as e:
                print(f"Error in font inline_handler: {e}")

    @bot.on(events.CallbackQuery(pattern=r'font_([^_]+)_(.*)'))
    async def font_callback(event):
        try:
            # Handle both string and bytes input
            font_style = event.pattern_match.group(1)
            if isinstance(font_style, bytes):
                font_style = font_style.decode('utf-8')
                
            text = event.pattern_match.group(2)
            if isinstance(text, bytes):
                text = text.decode('utf-8')
            
            # Get the font mapping
            font_mapping = FONT_MAP.get(font_style, {})
            
            # Generate the styled text
            converted = ''.join([font_mapping.get(char, char) for char in text])
            await event.edit(converted)
        except Exception as e:
            print(f"Error in font_callback: {e}")
            await event.answer("âš ï¸ Gagal mengkonversi font", alert=True)

    @bot.on(events.CallbackQuery(pattern=r'fontpage_(\d+)_(.*)'))
    async def font_page_callback(event):
        try:
            page = int(event.pattern_match.group(1))
            text = event.pattern_match.group(2)
            if isinstance(text, bytes):
                text = text.decode('utf-8')
            
            await event.edit(
                text=create_font_caption(text, (await event.get_sender()).first_name),
                buttons=get_font_markup(text, page)
            )
        except Exception as e:
            print(f"Error in font_page_callback: {e}")
            await event.answer("âš ï¸ Gagal memuat halaman", alert=True)

    @bot.on(events.CallbackQuery(pattern=r'close'))
    async def close_callback(event):
        try:
            await event.delete()
        except:
            try:
                await event.answer("Pesan sudah dihapus")
            except:
                pass

    # Userbot handler: trigger inline
    @user.on(events.NewMessage(outgoing=True, from_users=OWNER_ID))
    async def font_command_handler(event):
        msg = (event.text or '').strip()
        current_prefix = get_live_prefix()

        is_font_cmd = False
        text = ""
        
        if current_prefix == "no":
            if msg.lower().startswith("font "):
                is_font_cmd = True
                text = msg[5:].strip()
        else:
            if msg.startswith(current_prefix):
                cmd = msg[len(current_prefix):].strip().lower()
                if cmd.startswith("font "):
                    is_font_cmd = True
                    text = cmd[5:].strip()

        if not is_font_cmd or not text:
            return

        try:
            await event.delete()
            result = await user.inline_query(BOT_USERNAME, f"font {text}")
            if result:
                await result[0].click(event.chat_id)
        except Exception as e:
            print(f"Error in font_command_handler: {e}")
            await event.respond("âš ï¸ Gagal memunculkan menu font")